import os
import torch
import numpy as np
import pandas as pd
from torch.utils.data import Dataset
from tqdm import tqdm
from audioProcessor import AudioProcessor
from hparams import HParams

class DatasetPreprocessor:
    def __init__(self, metadata_path: str, wavs_dir: str, output_dir: str, hparams: HParams):
        """
        Handles full preprocessing from WAV to Mel-spectrograms.
        """
        self.metadata_path = metadata_path
        self.wavs_dir = wavs_dir
        self.output_dir = output_dir
        self.ap = AudioProcessor(hparams)
        
        self.mels_dir = os.path.join(output_dir, "mels")
        os.makedirs(self.mels_dir, exist_ok=True)

    def preprocess(self):
        print(f"Starting dataset preprocessing into {self.output_dir}...")
        data = []
        with open(self.metadata_path, 'r', encoding='utf-8') as f:
            for line in f:
                parts = line.strip().split('|')
                if len(parts) < 2:
                    continue
                file_id = parts[0]
                text = parts[1]
                data.append({'id': file_id, 'text': text})
        
        df = pd.DataFrame(data)
        processed_metadata_path = os.path.join(self.output_dir, "processed_metadata.csv")
        
        valid_data = []
        for _, row in tqdm(df.iterrows(), total=len(df), desc="Processing WAVs to Mel"):
            file_id = row['id']
            wav_path = os.path.join(self.wavs_dir, f"{file_id}.wav")
            
            if not os.path.exists(wav_path):
                continue
                
            try:
                # Convert audio to mel spectrogram
                mel = self.ap.wav_to_mel_from_path(wav_path)
                
                # Save mel as numpy array
                mel_path = os.path.join(self.mels_dir, f"{file_id}.npy")
                np.save(mel_path, mel)
                
                valid_data.append({
                    'id': file_id,
                    'text': row['text'],
                    'mel_path': mel_path
                })
            except Exception as e:
                print(f"Failed to process {file_id}: {e}")
                
        # Save new processed metadata
        pd.DataFrame(valid_data).to_csv(processed_metadata_path, index=False)
        print(f"Preprocessing finished. Processed metadata saved to {processed_metadata_path}")


class SpeechDataset(Dataset):
    def __init__(self, processed_metadata_path: str):
        """
        PyTorch Dataset for loading the processed mels and text.
        processed_metadata_path: Path to the CSV generated by DatasetPreprocessor
        """
        # Load the processed metadata
        self.df = pd.read_csv(processed_metadata_path)

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        file_id = row['id']
        text = row['text']
        mel_path = row['mel_path']
        
        # Load mel spectrogram
        mel = np.load(mel_path)
        mel_tensor = torch.FloatTensor(mel)
        
        return {
            'id': file_id,
            'text': text,
            'mel': mel_tensor
        }

