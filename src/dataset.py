from duration import extract_durations_for_item
from torch.nn.utils.rnn import pad_sequence

class DatasetPreprocessor:
    def __init__(self, metadata_path: str, wavs_dir: str, output_dir: str, hparams: HParams):
        """
        Handles full preprocessing from WAV to Mel-spectrograms.
        """
        self.metadata_path = metadata_path
        self.wavs_dir = wavs_dir
        self.output_dir = output_dir
        self.ap = AudioProcessor(hparams)
        
        self.mels_dir = os.path.join(output_dir, "mels")
        os.makedirs(self.mels_dir, exist_ok=True)

    def preprocess(self):
        print(f"Starting dataset preprocessing into {self.output_dir}...")
        data = []
        with open(self.metadata_path, 'r', encoding='utf-8') as f:
            for line in f:
                parts = line.strip().split('|')
                if len(parts) < 2:
                    continue
                file_id = parts[0]
                text = parts[1]
                data.append({'id': file_id, 'text': text})
        
        df = pd.DataFrame(data)
        processed_metadata_path = os.path.join(self.output_dir, "processed_metadata.csv")
        
        valid_data = []
        for _, row in tqdm(df.iterrows(), total=len(df), desc="Processing WAVs to Mel"):
            file_id = row['id']
            wav_path = os.path.join(self.wavs_dir, f"{file_id}.wav")
            
            if not os.path.exists(wav_path):
                continue
                
            try:
                # Convert audio to mel spectrogram
                mel = self.ap.wav_to_mel_from_path(wav_path)
                
                # Save mel as numpy array
                mel_path = os.path.join(self.mels_dir, f"{file_id}.npy")
                np.save(mel_path, mel)
                
                valid_data.append({
                    'id': file_id,
                    'text': row['text'],
                    'mel_path': mel_path
                })
            except Exception as e:
                print(f"Failed to process {file_id}: {e}")
                
        # Save new processed metadata
        pd.DataFrame(valid_data).to_csv(processed_metadata_path, index=False)
        print(f"Preprocessing finished. Processed metadata saved to {processed_metadata_path}")


class SpeechDataset(Dataset):
    def __init__(self, processed_metadata_path: str, alignments_dir: Optional[str] = None):
        """
        PyTorch Dataset for loading the processed mels and text.
        processed_metadata_path: Path to the CSV generated by DatasetPreprocessor
        alignments_dir: Optional path to directory with MFA alignments (.npy).
        """
        # Load the processed metadata
        self.df = pd.read_csv(processed_metadata_path)
        self.alignments_dir = alignments_dir

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        file_id = row['id']
        text = row['text']
        mel_path = row['mel_path']
        # mel_path = str(row['mel_path']).replace('\\', '/')
        
        # Text to sequence
        text_seq = text_to_sequence(text)
        
        # Load mel spectrogram
        mel = np.load(mel_path)
        mel_tensor = torch.FloatTensor(mel)
        
        # Extract durations
        duration_tensor = extract_durations_for_item(
            text_seq=text_seq, 
            mel_tensor=mel_tensor, 
            file_id=file_id, 
            alignments_dir=self.alignments_dir
        )
        
        return {
            'id': file_id,
            'text': text,
            'text_seq': torch.LongTensor(text_seq),
            'mel': mel_tensor,
            'duration': duration_tensor
        }

class SpeechCollate:
    def __call__(self, batch):
        ids = [item['id'] for item in batch]
        texts = [item['text'] for item in batch]
        
        text_seqs = [item['text_seq'] for item in batch]
        mels = [item['mel'] for item in batch]
        durations = [item['duration'] for item in batch]
        
        # Pad sequences (batch_first=True)
        text_seqs_padded = pad_sequence(text_seqs, batch_first=True, padding_value=0)
        durations_padded = pad_sequence(durations, batch_first=True, padding_value=0)
        
        # Pad mels
        # mels have shape (n_mels, time)
        max_mel_len = max([m.shape[1] for m in mels])
        n_mels = mels[0].shape[0]
        
        mels_padded = torch.zeros(len(batch), n_mels, max_mel_len)
        stop_tokens = torch.ones(len(batch), max_mel_len)
        for i, m in enumerate(mels):
            mel_len = m.shape[1]
            mels_padded[i, :, :mel_len] = m
            stop_tokens[i, :mel_len] = 0
            
        return {
            'ids': ids,
            'texts': texts,
            'text_seqs': text_seqs_padded,
            'mels': mels_padded,
            'durations': durations_padded,
            'stop_tokens': stop_tokens
        }

